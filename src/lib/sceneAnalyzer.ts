// ─────────────────────────────────────────────────────────────────────────────
// § 6  NORMALISATION HELPERS
// ─────────────────────────────────────────────────────────────────────────────

function normaliseLocationName(name: string): string {
  return name.toLowerCase().trim()
    .replace(/\b(the|a|an)\b/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function characterId(name: string): string {
  return `char-${name.toLowerCase().replace(/\s+/g, '-')}`;
}

function locationId(name: string): string {
  return `loc-${name.toLowerCase().replace(/\s+/g, '-')}`;
}

// ─────────────────────────────────────────────────────────────────────────────
// § 7  SCENE CARD BUILDER
// ─────────────────────────────────────────────────────────────────────────────

function buildSceneCards(
  scenes: SceneRaw[],
  sceneNumberOffset: number,
  characterMap: Map<string, Character>,
  locationMap: Map<string, Location>,
  locationNormMap: Map<string, string>,
  timeContextLabelMap: Map<string, string>,
  fullText: string,
  searchState: { lastIndex: number },
): SceneCard[] {
  return scenes.map((scene, idx) => {
    const globalIdx  = sceneNumberOffset + idx;
    const id         = crypto.randomUUID();
    const charIds: string[]  = [];
    const locIds: string[]   = [];

    // Link characters by name
    scene.characterNames?.forEach(name => {
      const cid = characterId(name);
      if (characterMap.has(cid) && !charIds.includes(cid)) charIds.push(cid);
    });

    // Inline character definitions (sometimes AI embeds them per-scene)
    scene.characters?.forEach(char => {
      if (!char.name) return;
      const cid = characterId(char.name);
      if (!characterMap.has(cid)) {
        characterMap.set(cid, {
          id: cid, name: char.name, role: char.role,
          isCrowd: char.isCrowd ?? false,
          age: char.age, ethnicity: char.ethnicity,
          physicalFeatures: char.physicalFeatures,
          hair: char.hair, beard: char.beard, clothing: char.clothing,
          visualDescription: char.visualDescription,
        });
      }
      if (!charIds.includes(cid)) charIds.push(cid);
    });

    // Link locations by normalised name
    scene.locationNames?.forEach(name => {
      const norm = normaliseLocationName(name);
      const lid  = locationNormMap.get(norm);
      if (lid && !locIds.includes(lid)) locIds.push(lid);
    });

    scene.locations?.forEach(loc => {
      if (!loc.name) return;
      const norm = normaliseLocationName(loc.name);
      const existing = locationNormMap.get(norm);
      if (existing) {
        if (!locIds.includes(existing)) locIds.push(existing);
      } else {
        const lid = locationId(loc.name);
        locationMap.set(lid, { id: lid, name: loc.name, visualDescription: loc.visualDescription });
        locationNormMap.set(norm, lid);
        if (!locIds.includes(lid)) locIds.push(lid);
      }
    });

    // Find text position in source
    let startIndex: number | undefined;
    let endIndex: number | undefined;
    if (scene.text) {
      const match = fuzzyFindText(fullText, scene.text, searchState.lastIndex);
      if (match) {
        startIndex = match.start;
        endIndex   = match.end;
        searchState.lastIndex = match.end;
      }
    }

    const card: SceneCard = {
      id,
      sceneNumber: globalIdx + 1,
      text: scene.text ?? '',
      visualNote: scene.visualNote ?? `Scene ${globalIdx + 1}`,
      narrativeLayer: scene.narrativeLayer,
      visualStyle: resolveVisualStyle(scene),
      characterIds: charIds,
      locationIds: locIds,
      timeContextIds: [],
      startIndex,
      endIndex,
      prompts: [],
      status: 'analyzed',
      noteEditable: true,
    };

    if (scene.timeContextLabel) {
      timeContextLabelMap.set(id, scene.timeContextLabel);
    }

    return card;
  });
}

function resolveVisualStyle(scene: SceneRaw): 'cinematic' | 'symbolic' | 'scientific' | 'abstract' | undefined {
  if (scene.visualStyle === 'symbolic')   return 'symbolic';
  if (scene.visualStyle === 'scientific') return 'scientific';
  if (scene.narrativeLayer === 'scientific') return 'scientific';

  // Keyword detection for symbolic content
  const combined = `${scene.visualNote ?? ''} ${scene.text ?? ''}`.toLowerCase();
  const symbolicKeywords = [
    'energy ring', 'cosmic force', 'frozen time', 'frozen water', 'shape transforms',
    'spirit appears', 'deity descends', 'divine light', 'shape-shifts', 'metamorphosis',
    'sound wave visible', 'vibration visible', 'energy field', 'aura radiates',
  ];
  if (symbolicKeywords.some(k => combined.includes(k))) return 'symbolic';

  return 'cinematic';
}

// ─────────────────────────────────────────────────────────────────────────────
// § 8  CHUNK ANALYSER
// ─────────────────────────────────────────────────────────────────────────────

async function analyseChunk(
  chunk: string,
  targetSceneCount?: number,
): Promise<AnalysisPayload> {
  const systemPrompt = getSceneAnalysisSystemPrompt();
  
  // Inject target instruction into User Message to preserve System Prompt caching
  let userMessage = chunk;
  if (targetSceneCount) {
    userMessage = `${getSceneAnalysisTargetInstruction(targetSceneCount)}\n\n--- TEXT TO ANALYZE ---\n${chunk}`;
  }

  const raw = await aiProvider.generateContent(userMessage, systemPrompt, { operationType: 'scene_analysis' });

  let cleaned: string;
  try {
    cleaned = cleanJsonResponse(raw);
  } catch {
    cleaned = raw;
  }

  try {
    return JSON.parse(cleaned) as AnalysisPayload;
  } catch (err) {
    try {
      return JSON.parse(recoverTruncatedJson(cleaned)) as AnalysisPayload;
    } catch {
      throw new Error(`Scene analysis JSON parse failed: ${err instanceof Error ? err.message : err}`);
    }
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// § 9  TIME CONTEXT MERGE
// ─────────────────────────────────────────────────────────────────────────────

function mergeTimeContexts(existing: TimeContext[], incoming: TimeContext[]): TimeContext[] {
  const map = new Map(existing.map(tc => [tc.label, tc]));
  incoming.forEach(tc => { if (!map.has(tc.label)) map.set(tc.label, tc); });
  return Array.from(map.values());
}

// ─────────────────────────────────────────────────────────────────────────────
// § 10  MAIN ANALYSIS FUNCTION
// ─────────────────────────────────────────────────────────────────────────────

export async function analyzeTextIntoScenes(
  text: string,
  _apiKey?: string,
  _model?: string,
  onProgress?: (message: string) => void,
  targetSceneCount?: number,
): Promise<{
  sceneCards: SceneCard[];
  characters: Character[];
  locations: Location[];
  suggestedTimeContexts: TimeContext[];
}> {
  onProgress?.(`Preparing text... (${text.length} characters, ~${Math.round(text.split(' ').length)} words)`);

  const chunks = splitTextIntoChunks(text);
  onProgress?.(`Split into ${chunks.length} chunk(s) for analysis`);

  const characterMap       = new Map<string, Character>();
  const locationMap        = new Map<string, Location>();
  const locationNormMap    = new Map<string, string>(); // normalised name → id
  const timeContextLabelMap = new Map<string, string>(); // sceneId → label
  const allSceneCards: SceneCard[] = [];
  let suggestedTimeContexts: TimeContext[] = [];
  let sceneNumberOffset = 0;
  const searchState = { lastIndex: 0 };

  // Distribute target scene count proportionally across chunks
  const totalChars = chunks.reduce((s, c) => s + c.length, 0);
  let remainingTarget = targetSceneCount;
  
  const chunkTargets: (number | undefined)[] = chunks.map((chunk, i) => {
    if (!targetSceneCount) return undefined;
    if (chunks.length === 1) return targetSceneCount;
    
    // Proportional target
    let target = Math.round(targetSceneCount * (chunk.length / totalChars));
    
    // Ensure at least 1 scene per chunk if the total target allows it
    if (target < 1 && remainingTarget >= 1) target = 1;
    
    // Last chunk gets the remainder to ensure exact total
    if (i === chunks.length - 1) {
      return Math.max(1, remainingTarget);
    }
    
    remainingTarget -= target;
    return Math.max(1, target);
  });

  console.log(`[sceneAnalyzer] Analysis started: ${chunks.length} chunks. Targets:`, chunkTargets);
  onProgress?.(`Metin ${chunks.length} parçaya bölündü, analiz başlatılıyor...`);

  // Process chunks IN PARALLEL for massive speedup
  const chunkResults = await Promise.all(
    chunks.map(async (chunk, i) => {
      try {
        const result = await analyseChunk(chunk, chunkTargets[i]);
        onProgress?.(`Parça ${i + 1}/${chunks.length} tamamlandı (${result.scenes?.length || 0} sahne).`);
        return result;
      } catch (err) {
        console.error(`Error analyzing chunk ${i}:`, err);
        throw err;
      }
    })
  );

  for (let i = 0; i < chunkResults.length; i++) {
    const parsed = chunkResults[i];

    // Register characters
    (parsed.characters ?? []).forEach(char => {
      if (!char.name) return;
      const cid = characterId(char.name);
      if (!characterMap.has(cid)) {
        characterMap.set(cid, {
          id: cid, name: char.name, role: char.role,
          isCrowd: char.isCrowd ?? false,
          age: char.age, ethnicity: char.ethnicity,
          physicalFeatures: char.physicalFeatures,
          hair: char.hair, beard: char.beard, clothing: char.clothing,
          visualDescription: char.visualDescription,
        });
      }
    });

    // Register locations
    (parsed.locations ?? []).forEach(loc => {
      if (!loc.name) return;
      const norm = normaliseLocationName(loc.name);
      if (!locationNormMap.has(norm)) {
        const lid = locationId(loc.name);
        locationMap.set(lid, { id: lid, name: loc.name, visualDescription: loc.visualDescription });
        locationNormMap.set(norm, lid);
      }
    });

    // Build location normalised index (for card builder)
    const locNormSnap = new Map(locationNormMap);

    // Reset search anchor to start of this chunk
    const chunkStart = text.indexOf(chunks[i]);
    if (chunkStart !== -1) searchState.lastIndex = chunkStart;

    const cards = buildSceneCards(
      parsed.scenes ?? [],
      sceneNumberOffset,
      characterMap,
      locationMap,
      locNormSnap,
      timeContextLabelMap,
      text,
      searchState,
    );

    allSceneCards.push(...cards);
    sceneNumberOffset += (parsed.scenes ?? []).length;

    onProgress?.(`${cards.length} scene cards built from chunk ${i + 1}`);

    // Collect time contexts
    const rawContexts: TimeContextRaw[] =
      Array.isArray(parsed.timeContexts) && parsed.timeContexts.length > 0
        ? parsed.timeContexts
        : parsed.timeContext?.label ? [parsed.timeContext] : [];

    const chunkContexts: TimeContext[] = rawContexts
      .filter(tc => tc.label)
      .map(tc => ({
        id: `tc-${crypto.randomUUID()}`,
        label: tc.label!,
        era: tc.era,
        season: tc.season,
        timeOfDay: tc.timeOfDay,
        lighting: tc.lighting,
        weather: tc.weather,
        historicalNotes: tc.historicalNotes,
      }));

    suggestedTimeContexts = mergeTimeContexts(suggestedTimeContexts, chunkContexts);
  }

  // Link scene cards to time contexts
  const labelToId = new Map(suggestedTimeContexts.map(tc => [tc.label, tc.id]));
  for (const card of allSceneCards) {
    const label = timeContextLabelMap.get(card.id);
    if (label) {
      const tcId = labelToId.get(label);
      if (tcId) card.timeContextIds = [tcId];
    }
  }

  onProgress?.(`Analysis complete — ${allSceneCards.length} scenes, ${characterMap.size} characters, ${locationMap.size} locations`);

  return {
    sceneCards: allSceneCards,
    characters: Array.from(characterMap.values()),
    locations: Array.from(locationMap.values()),
    suggestedTimeContexts,
  };
}

