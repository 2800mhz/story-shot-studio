// § 4  FUZZY TEXT POSITION FINDER
//      Locates a scene's source text within the full narration for highlighting.
// ─────────────────────────────────────────────────────────────────────────────

function fuzzyFindText(
  fullText: string,
  searchStr: string,
  startIndex: number,
): { start: number; end: number } | null {
  const clean = searchStr.trim();
  if (!clean) return null;

  // Exact match from startIndex
  let idx = fullText.indexOf(clean, startIndex);
  if (idx !== -1) return { start: idx, end: idx + clean.length };

  // Exact match from beginning
  idx = fullText.indexOf(clean, 0);
  if (idx !== -1) return { start: idx, end: idx + clean.length };

  // Case-insensitive
  const lower = fullText.toLowerCase();
  const lowerSearch = clean.toLowerCase();
  idx = lower.indexOf(lowerSearch, startIndex);
  if (idx === -1) idx = lower.indexOf(lowerSearch, 0);
  if (idx !== -1) return { start: idx, end: idx + clean.length };

  // Token fuzzy match (80% threshold)
  const tokenRe = /\S+/g;
  const fullTokens: { word: string; index: number }[] = [];
  let m: RegExpExecArray | null;
  while ((m = tokenRe.exec(fullText)) !== null) {
    fullTokens.push({ word: m[0].toLowerCase().replace(/\W/g, ''), index: m.index });
  }

  const searchTokens = clean.split(/\s+/)
    .map(w => w.toLowerCase().replace(/\W/g, ''))
    .filter(w => w.length > 0);

  if (!searchTokens.length) return null;

  const threshold = Math.ceil(searchTokens.length * 0.8);
  const fromIdx   = fullTokens.findIndex(t => t.index >= Math.max(0, startIndex - 50));
  const from      = fromIdx >= 0 ? fromIdx : 0;

  const tryFrom = (start: number, end: number) => {
    for (let i = start; i <= end - searchTokens.length; i++) {
      let matches = 0;
      for (let j = 0; j < searchTokens.length; j++) {
        const ft = fullTokens[i + j]?.word ?? '';
        const st = searchTokens[j];
        if (ft === st || ft.startsWith(st) || st.startsWith(ft)) matches++;
      }
      if (matches >= threshold) {
        const s = fullTokens[i].index;
        return { start: s, end: Math.min(s + clean.length + 10, fullText.length) };
      }
    }
    return null;
  };

  return tryFrom(from, fullTokens.length) ?? tryFrom(0, from);
}

