function cleanJsonResponse(text: string): string {
  const fenced = text.match(/```json\s*([\s\S]*?)```/);
  if (fenced) {
    text = fenced[1].trim();
  } else {
    const brace = text.indexOf('{');
    if (brace > 0) text = text.substring(brace);
    text = text.replace(/```\s*$/g, '').trim();
  }

  // Strip non-printable control characters (keep tab/LF/CR)
  text = text.replace(/[\u0000-\u0008\u000B\u000C\u000E-\u001F\u007F-\u009F]/g, '');

  // Escape unescaped whitespace inside JSON strings
  text = text.replace(/"([^"\\]*(\\.[^"\\]*)*)"/g, (m) =>
    m.replace(/\n/g, '\\n').replace(/\r/g, '\\r').replace(/\t/g, '\\t')
  );

  try {
    JSON.parse(text);
    return text;
  } catch {
    return recoverTruncatedJson(text);
  }
}

function recoverTruncatedJson(raw: string): string {
  const lastBrace = raw.lastIndexOf('}');
  if (lastBrace <= 0) throw new Error('Cannot recover truncated JSON — no closing brace found');

  let candidate = raw.substring(0, lastBrace + 1);
  let brackets = 0, braces = 0, inStr = false, esc = false;

  for (const ch of candidate) {
    if (esc)  { esc = false; continue; }
    if (ch === '\\' && inStr) { esc = true; continue; }
    if (ch === '"') { inStr = !inStr; continue; }
    if (inStr) continue;
    if (ch === '[') brackets++;
    else if (ch === ']') brackets--;
    else if (ch === '{') braces++;
    else if (ch === '}') braces--;
  }

  candidate += ']'.repeat(Math.max(0, brackets));
  candidate += '}'.repeat(Math.max(0, braces));
  return candidate;
}

